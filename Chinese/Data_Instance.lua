                    [5] = "重要",
                    [6] = "可打断技能",
                    [7] = "法术效果",
                    [8] = "诅咒效果",
                    [9] = "中毒效果",
                }
                if tab[iconFlag] then
                    icon.tooltipTitle = tab[iconFlag]--_G["ENCOUNTER_JOURNAL_SECTION_FLAG"..iconFlag]
