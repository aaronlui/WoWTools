local function set(label, text, isHook)
    if label then
        text= text or label:GetText()
        text= e.strText[text]
        if text then
            label:SetText(text)
        end
        if isHook then
            hooksecurefunc(label, 'SetText', function(frame, name)
                name= e.strText[name]
                if name then
                    frame:SetText(name)
                end
            end)
        end
    end
end






