        for index2, icon in ipairs(infoHeaderButton.icons or {}) do
            local iconFlag = iconFlags and iconFlags[index2]
            if iconFlag then
                local tab={
                    [0] = "坦克预警",
                    [1] = "伤害输出预警",
                    [10] = "疾病效果",
                    [11] = "激怒",
